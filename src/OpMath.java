public class OpMath {
    public static void main(String[] args) {
        int a = 100;
        int b = 2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b+1);

        int c = 3;

        c += 2;
        System.out.println(c);

        c -= 2;
        System.out.println(c);

        c *= 2;
        System.out.println(c);

        int d = 11;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);
    }
}
